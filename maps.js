// Funzione di utilità per verificare le collisioni con i muri
function isWallAt(x, y) {
    // Trasforma le coordinate continue in indici della griglia
    let gridX = Math.floor(x / MAP_CELL_SIZE);
    let gridY = Math.floor(y / MAP_CELL_SIZE);

    // Se fuori dai confini della mappa, consideralo un muro per sicurezza
    if (gridX < 0 || gridX >= MAP_WIDTH || gridY < 0 || gridY >= MAP_HEIGHT) {
        return true;
    }
    
    return MAP_GRID[gridY][gridX] !== 0;
}
